"""
interactive_comparison.py
=========================
An interactive terminal script that lets you type any PyTorch coding prompt
and instantly see the code generated by BOTH systems side-by-side:

  1. OUR SYSTEM   — Ollama LLM + 24K-node Graph-RAG Context
  2. STANDALONE   — Ollama LLM with ZERO context (pure memory)

Usage:
  python benchmark/interactive_comparison.py --model llama3.2
"""

import argparse
import sys
from pathlib import Path

# Add project root to path
ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT))

from src.research_pipeline.pipeline import CsvFirstResearchPipeline
from benchmark.run_comparison import call_ollama  # Reusing the raw Ollama caller

def main():
    parser = argparse.ArgumentParser(description="Interactive Head-to-Head Comparison")
    parser.add_argument("--model", default="llama3.2", help="Which Ollama model to use for generation")
    args = parser.parse_args()
    model = args.model

    print("\n" + "="*80)
    print(" 🚀 INITIALIZING STRUCTURAL-CODER GRAPH-RAG ENGINE")
    print("="*80)
    print(f"Loading 24,000+ PyTorch nodes and training GNN embeddings...")
    
    # Initialize pipeline
    pipeline = CsvFirstResearchPipeline(
        nodes_csv=str(ROOT / "data/nodes.csv"),
        edges_csv=str(ROOT / "data/edges.csv"),
        embedding_cache=str(ROOT / "artifacts/research_gnn_embeddings.json"),
    )
    pipeline.step3_gnn_training()
    
    print("✅ System Ready!\n")

    # Interactive Loop
    while True:
        try:
            query = input("\n📝 Enter your PyTorch query (or type 'exit' to quit): ").strip()
            if not query:
                continue
            if query.lower() in ['exit', 'quit']:
                print("Goodbye!")
                break
                
            print("\n" + "="*80)
            print(f" ⏳ GENERATING CODE FOR: \"{query}\"")
            print("="*80)
            
            # -----------------------------------------------------------------
            # 1. OUR SYSTEM (Graph-RAG + LLM)
            # -----------------------------------------------------------------
            print("\n🔍 [1/2] structural-coder is searching the knowledge graph...")
            gnn_ctx = pipeline._gnn_retriever.retrieve(query=query, top_k=20, seed_k=4, expansion_hops=1)
            
            print(f"🧠 Found relevant APIs: {', '.join(gnn_ctx.seed_nodes[:5])}")
            print(f"🤖 Generating code using {model} WITH Graph-RAG context...")
            
            our_result = pipeline._generate_answer_for_context(
                query=query, 
                nodes=gnn_ctx.nodes, 
                model=model, 
                use_ollama=True
            )
            our_answer = our_result["answer"]
            
            # -----------------------------------------------------------------
            # 2. STANDALONE LLM (No Context)
            # -----------------------------------------------------------------
            print(f"\n🤖 [2/2] Standalone: Generating code using {model} only (NO context)...")
            raw_prompt = (
                f"You are a PyTorch 2.x coding expert. "
                f"Write complete, runnable Python code with proper imports for the following:\n\n{query}\n\n"
                f"Use ```python code blocks."
            )
            their_answer = call_ollama(raw_prompt, model)
            
            # Compute Scores (Our System)
            our_retrieval = pipeline._token_hit_score(query, gnn_ctx.nodes)
            our_grounding = pipeline._grounding_score(our_answer, gnn_ctx.nodes, query=query)
            our_validity = pipeline._code_validity_score(our_answer, target_hardware="H100")
            our_final = 0.4 * our_retrieval + 0.6 * (0.5 * our_grounding + 0.5 * our_validity)
            
            # Compute Scores (Standalone LLM)
            # Standalone has no retrieval (0.0), and we don't grade its grounding
            # against our retrieved nodes since it never saw them — that would be unfair.
            their_retrieval = 0.0
            their_grounding = 0.0  # N/A — standalone never saw retrieved context
            their_validity = pipeline._code_validity_score(their_answer, target_hardware="H100")
            their_final = 0.6 * (0.5 * their_grounding + 0.5 * their_validity)
            
            # -----------------------------------------------------------------
            # PRINT RESULTS
            # -----------------------------------------------------------------
            our_hdr = f"🏆 OUR SYSTEM │ Ret: {our_retrieval*100:.0f}% │ Grnd: {our_grounding*100:.0f}% │ Val: {our_validity*100:.0f}% │ Final: {our_final*100:.0f}%"
            print("\n\n" + our_hdr.center(80, "░"))
            print("\n" + our_answer.strip() + "\n")
            
            their_hdr = f"❌ STANDALONE │ Ret: {their_retrieval*100:.0f}% │ Grnd: {their_grounding*100:.0f}% │ Val: {their_validity*100:.0f}% │ Final: {their_final*100:.0f}%"
            print("\n" + their_hdr.center(80, "░"))
            print("\n" + their_answer.strip() + "\n")
            
            print("░"*80)
            
        except KeyboardInterrupt:
            print("\nGoodbye!")
            break

if __name__ == "__main__":
    main()
